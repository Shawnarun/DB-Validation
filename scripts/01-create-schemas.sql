-- Oracle Data Migration Validator - Database Initialization Script
-- This script creates the necessary schemas and sample data for testing

-- Create schemas
CREATE USER ATDM IDENTIFIED BY atdm_password;
CREATE USER OM IDENTIFIED BY om_password;
CREATE USER CCBS2 IDENTIFIED BY ccbs2_password;

-- Grant necessary privileges
GRANT CONNECT, RESOURCE, DBA TO ATDM;
GRANT CONNECT, RESOURCE, DBA TO OM;
GRANT CONNECT, RESOURCE, DBA TO CCBS2;

-- Grant additional privileges for cross-schema access
GRANT SELECT ANY TABLE TO ATDM;
GRANT SELECT ANY TABLE TO OM;
GRANT SELECT ANY TABLE TO CCBS2;

-- Create ATDM tables
CREATE TABLE ATDM.AT_DYN_1_CONNECTION_MIG (
    MOBILE_NO VARCHAR2(20) NOT NULL,
    CONNECTION_ID NUMBER(10),
    CREATED_DATE DATE DEFAULT SYSDATE
);

-- Create OM audit table
CREATE TABLE OM.TEMP_MIGRATION_AUDIT (
    MOBILE_NO     VARCHAR2(20),
    V1            VARCHAR2(10),
    V2            VARCHAR2(10),
    V3            VARCHAR2(10),
    V4            VARCHAR2(10),
    V5            VARCHAR2(10),
    CREATED_DATE  DATE DEFAULT SYSDATE
);

-- Create CCBS2 reference tables for validation
CREATE TABLE CCBS2.CAM_SUBSCRIBER_PROFILE (
    SUBSCRIBER_PROFILE_ID NUMBER(10) PRIMARY KEY,
    PROFILE_NAME VARCHAR2(100),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.CAM_SUBSCRIBER_NODE (
    SUBSCRIBER_NODE_ID NUMBER(10) PRIMARY KEY,
    SUBSCRIBER_PROFILE_ID NUMBER(10),
    NODE_NAME VARCHAR2(100),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.CAM_CONTRACT (
    CONTRACT_ID NUMBER(10) PRIMARY KEY,
    SUBSCRIBER_NODE_ID NUMBER(10),
    CONTRACT_TYPE VARCHAR2(50),
    STATUS VARCHAR2(10),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.PR_PRIORITY_TYPES (
    PRIORITY_TYPE_ID NUMBER(10) PRIMARY KEY,
    PRIORITY_NAME VARCHAR2(50),
    PRIORITY_LEVEL NUMBER(3)
);

CREATE TABLE CCBS2.PROV_SWITCH_IMAGE (
    IMSI_NO VARCHAR2(20) PRIMARY KEY,
    SWITCH_ID VARCHAR2(20),
    IMAGE_TYPE VARCHAR2(20),
    STATUS VARCHAR2(10),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.CAM_CONTRACT_PACKAGE (
    CONTRACT_PACKAGE_ID NUMBER(10) PRIMARY KEY,
    CONTRACT_ID NUMBER(10),
    PACKAGE_NAME VARCHAR2(100),
    PACKAGE_TYPE VARCHAR2(50),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.CAM_CONNECTION (
    CONNECTION_ID NUMBER(10) PRIMARY KEY,
    PACKAGE_CONTRACT_ID NUMBER(10),
    CONNECTION_TYPE VARCHAR2(50),
    STATUS VARCHAR2(10),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.DYN_1_CONNECTION (
    CONNECTION_ID NUMBER(10) PRIMARY KEY,
    MOBILE_NO VARCHAR2(20),
    IMSI VARCHAR2(20),
    SIM VARCHAR2(20),
    SERVICE_ID NUMBER(5),
    STATUS VARCHAR2(10),
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.PHONE_NO_REGISTER (
    PHONE_NO VARCHAR2(20) PRIMARY KEY,
    STATUS VARCHAR2(10),
    BLOCKED_STATUS VARCHAR2(10),
    ALLOCATED_DATE DATE,
    CREATED_DATE DATE DEFAULT SYSDATE
);

CREATE TABLE CCBS2.IMSI_SIM_REGISTER (
    IMSI VARCHAR2(20),
    SUBSTRSIM VARCHAR2(20),
    SIM_TYPE VARCHAR2(20),
    STATUS VARCHAR2(10),
    CREATED_DATE DATE DEFAULT SYSDATE,
    CONSTRAINT PK_IMSI_SIM PRIMARY KEY (IMSI, SUBSTRSIM)
);

-- Create indexes for performance
CREATE INDEX IDX_ATDM_MOBILE_NO ON ATDM.AT_DYN_1_CONNECTION_MIG(MOBILE_NO);
CREATE INDEX IDX_OM_AUDIT_MOBILE_NO ON OM.TEMP_MIGRATION_AUDIT(MOBILE_NO);
CREATE INDEX IDX_DYN_MOBILE_NO ON CCBS2.DYN_1_CONNECTION(MOBILE_NO);
CREATE INDEX IDX_PHONE_REG_PHONE_NO ON CCBS2.PHONE_NO_REGISTER(PHONE_NO);
CREATE INDEX IDX_PROV_SWITCH_IMSI ON CCBS2.PROV_SWITCH_IMAGE(IMSI_NO);

